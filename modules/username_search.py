def find_socials(u,p=None):
    r={}
    px={"http":p,"https":p} if p else None
    for k,v in SITES.items():
        try:
            x=requests.get(v.format(u),timeout=5,proxies=px,allow_redirects=True)
            if x.status_code in (200,301,302):
                r[k]=v.format(u)
        except:
            pass
    return r
